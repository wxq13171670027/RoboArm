% 起点和终点
P3 = [0.2, 0.09];
P4 = [0.2, -0.09];

% 生成10个插值参数 t ∈ [0, 1]
t = linspace(0, 1, 10);

% 线性插值计算 x 和 y 坐标
x = P3(1) + t * (P4(1) - P3(1));
y = P3(2) + t * (P4(2) - P3(2));

% 合并为 10×2 的点矩阵
points3 = [x', y'];

% 显示结果
disp('生成的10个点：');
disp(points3);