% 起点和终点
P1 = [0.35, 0];
P2 = [0.12, 0.14];

% 生成20个点（包括端点）
t = linspace(0, 1, 20);
x = P1(1) + t * (P2(1) - P1(1));
y = P1(2) + t * (P2(2) - P1(2));

% 合并为 N×2 的点数组
points = [x', y'];

% 显示结果
disp(points);
